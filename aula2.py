n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))

s = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2

print(
    f'A soma é: {s}\n'
    f'A subtração é: {sub}\n'
    f'A multiplicação é: {m}\n'
    f'A divisão é: {d}\n'
    f'A divisão inteira é: {di}\n'
    f'A esponenciação é: {e}'
)